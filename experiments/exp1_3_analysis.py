def run_all_analyses(
    results_dir: Path,
    task: str,
    output_dir: Path,
    strategies: list[str] | None = None,
    n_train: int | None = None,
    embedding_cache_dir: Path | None = None,
) -> dict[str, Any]:
    """Run all Experiment 1.3 analyses and save results.

    Parameters
    ----------
    results_dir : path to exp1_1 (or exp1_2) output root
    task : "k562" or "yeast"
    output_dir : where to write JSON results and figures
    strategies : optional filter; if None, auto-discover all
    n_train : training size to analyze; if None, use the largest available
    embedding_cache_dir : optional path to pre-computed embeddings

    Returns a summary dict of all analysis results.
    """
    output_dir.mkdir(parents=True, exist_ok=True)

    # Discover strategies
    if strategies is None:
        strategies = discover_strategies(results_dir)
    if not strategies:
        logger.error(f"No strategies found in {results_dir}")
        return {}
    logger.info(f"Strategies: {strategies}")

    # Determine training size
    if n_train is None:
        all_sizes: set[int] = set()
        for s in strategies:
            all_sizes.update(discover_training_sizes(results_dir, s))
        if not all_sizes:
            logger.error("No training sizes found")
            return {}
        n_train = max(all_sizes)
    logger.info(f"Analyzing training size: n={n_train:,}")

    # Load data for each strategy
    data_by_strategy: dict[str, dict[str, Any]] = {}
    for strat in strategies:
        d = load_strategy_data(results_dir, strat, n_train)
        if d is not None:
            data_by_strategy[strat] = d

    if not data_by_strategy:
        logger.error("No data loaded for any strategy")
        return {}

    logger.info(f"Loaded data for {len(data_by_strategy)} strategies")

    # Collect pool labels if available (use first strategy's labels as proxy,
    # or load a dedicated pool file if it exists)
    pool_labels = None
    pool_path = results_dir / "pool_labels.npz"
    if pool_path.exists():
        pool_data = np.load(pool_path, allow_pickle=True)
        pool_labels = pool_data["labels"]
        logger.info(f"Loaded pool labels from {pool_path}")
    else:
        # Use the union of all strategies' labels as a rough baseline
        all_labels = np.concatenate([d["labels"] for d in data_by_strategy.values()])
        pool_labels = all_labels
        logger.info("No pool_labels.npz found; using union of strategy labels as baseline")

    summary: dict[str, Any] = {
        "task": task,
        "n_train": n_train,
        "strategies": list(data_by_strategy.keys()),
    }

    # --- 1. Sequence Composition ---
    logger.info("Running sequence composition analysis...")
    composition_results: dict[str, dict] = {}
    for strat, d in data_by_strategy.items():
        composition_results[strat] = analyze_composition(d["sequences"])
    summary["composition"] = {
        strat: {
            "gc_mean": r["gc_mean"],
            "gc_std": r["gc_std"],
            "homopolymer_mean": r["homopolymer_mean"],
            "dinuc_freqs": r["dinuc_freqs"],
        }
        for strat, r in composition_results.items()
    }
    plot_gc_distributions(composition_results, output_dir)

    # --- 2. Expression Properties ---
    logger.info("Running expression property analysis...")
    expression_results: dict[str, dict] = {}
    for strat, d in data_by_strategy.items():
        expression_results[strat] = analyze_expression(d["labels"], pool_labels=pool_labels)
    summary["expression"] = expression_results

    labels_by_strategy = {strat: d["labels"] for strat, d in data_by_strategy.items()}
    plot_expression_distributions(labels_by_strategy, output_dir)

    # --- 3. TF Motif Analysis ---
    logger.info("Running TF motif analysis...")
    motif_results: dict[str, dict] = {}
    for strat, d in data_by_strategy.items():
        motif_results[strat] = analyze_motifs(d["sequences"], task)
    summary["motifs"] = {
        strat: {
            k: v
            for k, v in r.items()
            if k != "motif_mean_counts"  # too verbose for top-level summary
        }
        for strat, r in motif_results.items()
    }
    summary["motifs_detail"] = {strat: r["motif_mean_counts"] for strat, r in motif_results.items()}
    plot_motif_heatmap(motif_results, output_dir)

    # --- 4. Embedding Space Analysis ---
    # Only run if embedding caches are available
    if embedding_cache_dir is not None:
        logger.info("Running embedding space analysis...")
        embeddings_by_strategy: dict[str, np.ndarray] = {}
        for strat in data_by_strategy:
            emb_path = embedding_cache_dir / f"{strat}_n{n_train}_embeddings.npy"
            if emb_path.exists():
                embeddings_by_strategy[strat] = np.load(emb_path)
            else:
                logger.warning(f"Embedding cache not found: {emb_path}")
        if embeddings_by_strategy:
            emb_result = analyze_embeddings(embeddings_by_strategy)
            # Strip non-serializable arrays for JSON summary
            summary["embeddings"] = {
                name: metrics for name, metrics in emb_result.items() if name != "projection"
            }
            plot_embedding_projection(emb_result, output_dir)
        else:
            logger.info("No embedding caches found; skipping embedding analysis")
    else:
        logger.info("No --embedding-cache-dir provided; skipping embedding analysis")

    # --- 5. Inter-Strategy Overlap ---
    logger.info("Running inter-strategy overlap analysis...")
    sequences_by_strategy = {strat: d["sequences"] for strat, d in data_by_strategy.items()}
    overlap = analyze_overlap(sequences_by_strategy)
    summary["overlap"] = {
        "strategy_names": overlap["strategy_names"],
        "jaccard_matrix": overlap["jaccard_matrix"].tolist(),
    }
    plot_jaccard_heatmap(overlap, output_dir)

    # --- Save JSON summary ---
    json_path = output_dir / "analysis_summary.json"
    json_path.write_text(json.dumps(summary, indent=2, default=str))
    logger.info(f"Saved analysis summary to {json_path}")

    return summary


