    """Heatmap of mean motif counts per sequence across strategies."""
    names = sorted(motif_by_strategy.keys())
    if not names:
        return
    motif_names = motif_by_strategy[names[0]].get("motifs_scanned", [])
    if not motif_names:
        return

    mat = np.array(
        [
            [motif_by_strategy[strat]["motif_mean_counts"].get(m, 0) for m in motif_names]
            for strat in names
        ]
    )

    fig, ax = plt.subplots(figsize=(max(6, len(motif_names) * 0.5), max(4, len(names) * 0.4)))
    im = ax.imshow(mat, cmap="Blues", aspect="auto")
    ax.set_xticks(range(len(motif_names)))
    ax.set_yticks(range(len(names)))
    ax.set_xticklabels(motif_names, rotation=45, ha="right", fontsize=7)
    ax.set_yticklabels(names, fontsize=8)
    fig.colorbar(im, ax=ax, label="Mean count per sequence")
    ax.set_title("TF Motif Frequencies by Strategy")
    fig.tight_layout()
    fig.savefig(output_dir / "motif_heatmap.png", dpi=150)
    plt.close(fig)
    logger.info(f"Saved motif heatmap")


