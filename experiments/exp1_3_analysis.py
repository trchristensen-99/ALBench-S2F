# 2. Expression Property Analysis
# ---------------------------------------------------------------------------


def analyze_expression(
    labels: np.ndarray,
    pool_labels: np.ndarray | None = None,
) -> dict[str, Any]:
    """Analyze distribution of oracle-predicted expression levels.

    Parameters
    ----------
    labels : array of shape (N,) or (N, T)
        Oracle-predicted expression for selected sequences.
    pool_labels : optional array
        Pool baseline labels for enrichment calculation.

    Returns dict with distribution stats and enrichment metrics.
    """
    # Flatten to 1-D if multi-target (use mean across targets)
    if labels.ndim > 1:
        vals = labels.mean(axis=1)
    else:
        vals = labels

    result: dict[str, Any] = {
        "mean": float(np.mean(vals)),
        "std": float(np.std(vals)),
        "median": float(np.median(vals)),
        "min": float(np.min(vals)),
        "max": float(np.max(vals)),
        "q25": float(np.percentile(vals, 25)),
        "q75": float(np.percentile(vals, 75)),
        "iqr": float(np.percentile(vals, 75) - np.percentile(vals, 25)),
    }

    # Activity range coverage: divide pool range into 20 bins, measure fraction
    # of bins covered by at least one selected sequence.
    if pool_labels is not None:
        if pool_labels.ndim > 1:
            pool_vals = pool_labels.mean(axis=1)
        else:
            pool_vals = pool_labels
        pool_min, pool_max = float(np.min(pool_vals)), float(np.max(pool_vals))
        n_bins = 20
        bin_edges = np.linspace(pool_min, pool_max, n_bins + 1)
        bin_idx = np.digitize(vals, bin_edges) - 1
        bin_idx = np.clip(bin_idx, 0, n_bins - 1)
        bins_covered = len(set(bin_idx))
        result["range_coverage"] = bins_covered / n_bins

        # Enrichment: fraction in top/bottom 10% of pool
        p10 = np.percentile(pool_vals, 10)
        p90 = np.percentile(pool_vals, 90)
        result["frac_high"] = float(np.mean(vals >= p90))
        result["frac_low"] = float(np.mean(vals <= p10))
        result["pool_frac_high"] = 0.10  # baseline
        result["pool_frac_low"] = 0.10
        result["enrichment_high"] = result["frac_high"] / 0.10 if result["frac_high"] > 0 else 0.0
        result["enrichment_low"] = result["frac_low"] / 0.10 if result["frac_low"] > 0 else 0.0
    else:
        result["range_coverage"] = None
        result["enrichment_high"] = None
        result["enrichment_low"] = None

    return result


