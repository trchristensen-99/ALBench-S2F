# 5. Inter-Strategy Overlap
# ---------------------------------------------------------------------------


def analyze_overlap(sequences_by_strategy: dict[str, list[str]]) -> dict[str, Any]:
    """Compute pairwise Jaccard similarity between strategy-selected sequence sets.

    Returns a dict with a pairwise Jaccard matrix and strategy names.
    """
    names = sorted(sequences_by_strategy.keys())
    sets = {n: set(sequences_by_strategy[n]) for n in names}
    n = len(names)
    jaccard = np.zeros((n, n))
    for i in range(n):
        for j in range(n):
            si, sj = sets[names[i]], sets[names[j]]
            union = len(si | sj)
            jaccard[i, j] = len(si & sj) / union if union > 0 else 0.0

    return {
        "strategy_names": names,
        "jaccard_matrix": jaccard,
    }


