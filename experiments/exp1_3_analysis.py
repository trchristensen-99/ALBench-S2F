def _get_motifs_for_task(task: str) -> dict[str, str]:
    """Get combined motif dictionary for the given task.

    Merges the simple motif list from motif_planted with the richer library
    from motif_grammar.
    """
    if task == "k562":
        motifs = dict(K562_MOTIF_LIBRARY)
        # Add any extras from the simple list that aren't already covered
        for m in K562_MOTIFS:
            name = m[:6].upper()
            if m not in motifs.values():
                motifs[name] = m
    elif task == "yeast":
        motifs = dict(YEAST_MOTIF_LIBRARY)
        for m in YEAST_MOTIFS:
            name = m[:6].upper()
            if m not in motifs.values():
                motifs[name] = m
    else:
        raise ValueError(f"Unknown task: {task}")
    return motifs


def count_motifs_in_sequence(seq: str, motifs: dict[str, str]) -> dict[str, int]:
    """Count occurrences of each motif in a sequence (simple substring match).

    Skips motifs with IUPAC ambiguity codes (N, R, Y, etc.) — those require
    proper regex or FIMO for accurate matching.

    TODO: Replace with FIMO-based scanning for publication-quality results.
    """
    seq = seq.upper()
    counts: dict[str, int] = {}
    iupac_chars = set("NRYSWKMBDHV")
    for name, pattern in motifs.items():
        pattern = pattern.upper()
        if iupac_chars & set(pattern):
            # Skip ambiguous motifs — substring matching is inaccurate
            counts[name] = 0
            continue
        count = 0
        start = 0
        while True:
            idx = seq.find(pattern, start)
            if idx == -1:
                break
            count += 1
            start = idx + 1
        counts[name] = count
    return counts


def analyze_motifs(sequences: list[str], task: str) -> dict[str, Any]:
    """Motif analysis for a set of sequences.

    Returns:
    - motif_counts_per_seq: mean motif count per sequence (by motif name)
    - total_motif_hits: sum across all sequences
    - motif_diversity: mean number of distinct motif types per sequence
    - sequences_with_any_motif: fraction of sequences with >= 1 hit
    """
    motifs = _get_motifs_for_task(task)
    per_seq_counts = [count_motifs_in_sequence(s, motifs) for s in sequences]

    # Mean count per motif
    motif_names = sorted(motifs.keys())
    mean_counts = {
        name: float(np.mean([c.get(name, 0) for c in per_seq_counts])) for name in motif_names
    }

    # Diversity: distinct motifs per sequence
    diversities = [sum(1 for v in c.values() if v > 0) for c in per_seq_counts]

    # Total hits
    total_hits = {name: sum(c.get(name, 0) for c in per_seq_counts) for name in motif_names}

    any_motif = [any(v > 0 for v in c.values()) for c in per_seq_counts]

    return {
        "motif_mean_counts": mean_counts,
        "motif_total_hits": total_hits,
        "motif_diversity_mean": float(np.mean(diversities)),
        "motif_diversity_std": float(np.std(diversities)),
        "frac_with_any_motif": float(np.mean(any_motif)),
        "motifs_scanned": list(motif_names),
        "note": "Simple substring matching; IUPAC-ambiguous motifs skipped. "
        "TODO: integrate FIMO for PWM-based scanning.",
    }


# ---------------------------------------------------------------------------
# 4. Embedding Space Analysis
# ---------------------------------------------------------------------------


def analyze_embeddings(
    embeddings_by_strategy: dict[str, np.ndarray],
    pool_embeddings: np.ndarray | None = None,
    max_samples_umap: int = 5000,
) -> dict[str, Any]:
    """Embedding space analysis: UMAP/PCA visualization + distance metrics.

    Parameters
    ----------
    embeddings_by_strategy : dict mapping strategy name to (N, D) array
    pool_embeddings : optional (M, D) array for the training pool
    max_samples_umap : subsample cap for UMAP (memory / speed)

    Returns dict with distance metrics and 2-D projection coordinates.
    """
    result: dict[str, Any] = {}

    # Pool centroid
    if pool_embeddings is not None:
        centroid = pool_embeddings.mean(axis=0)
    else:
        # Use union of all strategies as fallback centroid
        all_embs = np.concatenate(list(embeddings_by_strategy.values()), axis=0)
        centroid = all_embs.mean(axis=0)

    # Per-strategy distance metrics
    for name, emb in embeddings_by_strategy.items():
        # Mean pairwise distance (subsample for speed)
        n = len(emb)
        if n > 2000:
            idx = np.random.default_rng(42).choice(n, 2000, replace=False)
            emb_sub = emb[idx]
        else:
            emb_sub = emb
        # Pairwise L2 distances
        diffs = emb_sub[:, None, :] - emb_sub[None, :, :]
        pw_dists = np.sqrt((diffs**2).sum(axis=-1))
        triu_idx = np.triu_indices(len(emb_sub), k=1)
        mean_pw_dist = float(pw_dists[triu_idx].mean())

        # Distance to centroid
        dists_to_centroid = np.sqrt(((emb - centroid) ** 2).sum(axis=1))
        mean_centroid_dist = float(dists_to_centroid.mean())

        result[name] = {
            "mean_pairwise_distance": mean_pw_dist,
            "mean_centroid_distance": mean_centroid_dist,
        }

    # 2-D projection (UMAP with PCA fallback)
    all_embs_list = []
    all_labels = []
    for name, emb in embeddings_by_strategy.items():
        # Subsample for visualization
        n = len(emb)
        per_strategy = max_samples_umap // max(len(embeddings_by_strategy), 1)
        if n > per_strategy:
            idx = np.random.default_rng(42).choice(n, per_strategy, replace=False)
            emb = emb[idx]
        all_embs_list.append(emb)
        all_labels.extend([name] * len(emb))
    all_embs = np.concatenate(all_embs_list, axis=0)

    projection_method = "pca"
    try:
        import umap

        reducer = umap.UMAP(n_components=2, random_state=42, n_neighbors=15, min_dist=0.1)
        coords_2d = reducer.fit_transform(all_embs)
        projection_method = "umap"
    except ImportError:
        logger.info("umap-learn not available, falling back to PCA")
        # Simple PCA via SVD
        centered = all_embs - all_embs.mean(axis=0)
        _, _, Vt = np.linalg.svd(centered, full_matrices=False)
        coords_2d = centered @ Vt[:2].T

    result["projection"] = {
        "method": projection_method,
        "coords": coords_2d,  # (N_total, 2) — not JSON-serializable, used for plotting
        "labels": all_labels,
    }

    return result


