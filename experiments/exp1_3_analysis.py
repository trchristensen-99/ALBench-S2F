def load_strategy_data(
    results_dir: Path,
    strategy: str,
    n_train: int,
) -> dict[str, Any] | None:
    """Load cached sequences + oracle labels for a strategy / training size.

    Returns dict with keys ``sequences`` (list[str]) and ``labels`` (np.ndarray),
    or *None* if the cache file does not exist.
    """
    cache_path = results_dir / strategy / f"n{n_train}" / "oracle_labels.npz"
    if not cache_path.exists():
        logger.warning(f"Cache not found: {cache_path}")
        return None
    data = np.load(cache_path, allow_pickle=True)
    sequences = data["sequences"].tolist()
    labels = data["labels"]
    logger.info(f"Loaded {len(sequences):,} seqs for {strategy}/n{n_train}")
    return {"sequences": sequences, "labels": labels}


def discover_strategies(results_dir: Path) -> list[str]:
    """Discover available strategy subdirectories in the results dir."""
    strategies = []
    for d in sorted(results_dir.iterdir()):
        if d.is_dir() and not d.name.startswith(("summary", ".", "_")):
            # Check that at least one oracle_labels.npz exists inside
            npz_files = list(d.glob("*/oracle_labels.npz"))
            if npz_files:
                strategies.append(d.name)
    return strategies


def discover_training_sizes(results_dir: Path, strategy: str) -> list[int]:
    """Discover available training sizes for a strategy."""
    sizes = []
    strategy_dir = results_dir / strategy
    if not strategy_dir.is_dir():
        return sizes
    for d in sorted(strategy_dir.iterdir()):
        if d.is_dir() and d.name.startswith("n"):
            try:
                n = int(d.name[1:])
                if (d / "oracle_labels.npz").exists():
                    sizes.append(n)
            except ValueError:
                pass
    return sorted(sizes)


# ---------------------------------------------------------------------------
# 1. Sequence Composition Analysis
# ---------------------------------------------------------------------------


def gc_content(seq: str) -> float:
    """Fraction of G + C bases in a sequence."""
    seq = seq.upper()
    gc = seq.count("G") + seq.count("C")
    return gc / len(seq) if len(seq) > 0 else 0.0


def dinucleotide_freqs(seq: str) -> dict[str, float]:
    """Normalized dinucleotide frequencies for a single sequence."""
    seq = seq.upper()
    counts = Counter(seq[i : i + 2] for i in range(len(seq) - 1))
    total = sum(counts[d] for d in DINUCLEOTIDES)
    if total == 0:
        return {d: 0.0 for d in DINUCLEOTIDES}
    return {d: counts.get(d, 0) / total for d in DINUCLEOTIDES}


def kmer_spectrum(seq: str, k: int) -> Counter:
    """Count k-mers in a single sequence."""
    seq = seq.upper()
    return Counter(seq[i : i + k] for i in range(len(seq) - k + 1))


def homopolymer_fraction(seq: str, min_run: int = 4) -> float:
    """Fraction of sequence covered by homopolymer runs >= *min_run* bp."""
    seq = seq.upper()
    if len(seq) == 0:
        return 0.0
    covered = 0
    i = 0
    while i < len(seq):
        j = i + 1
        while j < len(seq) and seq[j] == seq[i]:
            j += 1
        run_len = j - i
        if run_len >= min_run:
            covered += run_len
        i = j
    return covered / len(seq)


def analyze_composition(sequences: list[str]) -> dict[str, Any]:
    """Compute full sequence composition analysis for a set of sequences.

    Returns a dict with:
    - gc_contents: per-sequence GC content array
    - gc_mean, gc_std: summary stats
    - dinuc_freqs: mean dinucleotide frequencies (dict)
    - trimer_spectrum: mean 3-mer counts (dict)
    - tetramer_spectrum: mean 4-mer counts (dict)
    - homopolymer_fractions: per-sequence homopolymer fraction array
    - homopolymer_mean: mean fraction
    """
    gc_vals = np.array([gc_content(s) for s in sequences])

    # Dinucleotide frequencies (averaged)
    dinuc_list = [dinucleotide_freqs(s) for s in sequences]
    dinuc_mean = {d: float(np.mean([df[d] for df in dinuc_list])) for d in DINUCLEOTIDES}

    # k-mer spectra (averaged counts)
    tri_counts: Counter = Counter()
    tetra_counts: Counter = Counter()
    for s in sequences:
        tri_counts += kmer_spectrum(s, 3)
        tetra_counts += kmer_spectrum(s, 4)
    n = len(sequences)
    tri_mean = {k: tri_counts.get(k, 0) / n for k in TRIMERS}
    tetra_mean = {k: tetra_counts.get(k, 0) / n for k in TETRAMERS}

    hp_vals = np.array([homopolymer_fraction(s) for s in sequences])

    return {
        "gc_contents": gc_vals,
        "gc_mean": float(np.mean(gc_vals)),
        "gc_std": float(np.std(gc_vals)),
        "dinuc_freqs": dinuc_mean,
        "trimer_spectrum": tri_mean,
        "tetramer_spectrum": tetra_mean,
        "homopolymer_fractions": hp_vals,
        "homopolymer_mean": float(np.mean(hp_vals)),
    }


